public class DoWhileLoop{
	public static void main(String[] args) {
		int i = -111;
		do{
			System.out.print(i);
			i++;
			System.out.print(" ");
		}

		while(i<=-101);

		System.out.println(" ");

		i = 123;
		do{
			System.out.print(i);
			i--;
			System.out.print(" ");
		}

		while(i>=113);

		System.out.println(" ");

		i = 5;
		do{
			System.out.print(i);
			i--;
			System.out.print(" ");
		}

		while(i>= -5);

		System.out.println(" ");

		i = -10;
		do{
			System.out.print(i);
			i++;
			System.out.print(" ");
		}

		while(i<=0);

		System.out.println(" ");

		char j = 's';
		do{
			System.out.print(j);
			j--;
			System.out.print(" ");
		}

		while(j>= 'l');

		System.out.println(" ");

		j = 'T';
		do{
			System.out.print(j);
			j++;
			System.out.print(" ");
		}

		while(j<= 'Z');
	}
}